  const pairMap = new Map<string, string>()
    .set("(", ")")
    .set("[", "]")
    .set("{", "}")
    .set("<", ">");
  const opening = Array.from(pairMap.keys());
  const closing = Array.from(pairMap.values());
  const scoreMap = new Map<string, number>()
    .set("(", 1)
    .set("[", 2)
    .set("{", 3)
    .set("<", 4);

  const calCompleteScore = (
    line: string,
    index: number,
    openingList: string[]
  ): number | false => {
    const char = line[index];
    const lastOpening = openingList[0];
    if (char === undefined) {
    } else if (opening.includes(char)) {
      openingList.unshift(char);
      return calCompleteScore(line, index + 1, openingList);
    } else if (closing.includes(char)) {
      if (lastOpening !== undefined) {
        if (pairMap.get(lastOpening) === char) {
          openingList.shift();
          return calCompleteScore(line, index + 1, openingList);
        }
      }
      return false;
    }
    return openingList
      .map((char) => scoreMap.get(char) ?? 0)
      .reduce((acc, cur) => acc * 5 + cur, 0);
  };

